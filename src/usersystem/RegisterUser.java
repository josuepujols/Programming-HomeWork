    public RegisterUser(int Id) {
        initComponents();
        this.IsUpdating = true;
        buttonRegister.setText("Actualizar");
        this.id = Id;
        con = new Conection();
        conexion = con.getConexion();
        String query = "SELECT * FROM USERS WHERE ID = " + Id;
            Statement s;
            try {
                s = conexion.createStatement();
                ResultSet result = s.executeQuery(query);
            
                while (result.next()) {
                    //String Id = String.valueOf(result.getInt("ID"));
                    this.username.setText(result.getString("USERNAME"));
                    this.name.setText(result.getString("NAME"));
                    this.lastname.setText(result.getString("LASTNAME"));
                    this.number.setText(result.getString("PHONE"));
                    this.mail.setText(result.getString("MAIL"));
                    this.password1.setText(result.getString("PASSWORD"));
                    this.password2.setText(result.getString("PASSWORD"));
                }
            } 
            catch (SQLException ex) {
                Logger.getLogger(DashBoardUser.class.getName()).log(Level.SEVERE, null, ex);
            }
    }

