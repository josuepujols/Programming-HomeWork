    private void buttonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRegisterActionPerformed
        String userNameText = username.getText();
        String nameText = name.getText();
        String lastNameText = lastname.getText();
        String numberText = number.getText();
        String mailText = mail.getText();
        String pass1Text = new String(password1.getPassword());
        String pass2Text = new String(password2.getPassword());

        String Message = "";

        //Check if one field is not full
        if (userNameText.equals(new String("")) || userNameText.equals(new String("Nombre de Usuario"))) {
            Message =  "Nombre de Usuario, ";
            //JOptionPane.showMessageDialog(null, );
        }
        else if (nameText.equals(new String("")) || nameText.equals(new String("Nombre"))) {
            Message = "Nombre, ";
            //JOptionPane.showMessageDialog(null, "Debe llener el campo Nombre.");
        }
        else if (lastNameText.equals(new String("")) || lastNameText.equals(new String("Apellido"))) {
            Message = "Apellido, ";
            //JOptionPane.showMessageDialog(null, "Debe llener el campo Apellido.");
        }
        else if (numberText.equals(new String("")) || numberText.equals(new String("Telefono"))) {
            Message = "Telefono, ";
            //JOptionPane.showMessageDialog(null, "Debe llener el campo Telefono.");
        }
        else if (mailText.equals(new String("")) || mailText.equals(new String("Correo"))) {
            Message = "Correo, ";
            //JOptionPane.showMessageDialog(null, "Debe llener el campo Correo.");
        }
        else if (pass1Text.equals(new String(""))) {
            Message = "Contraseña, ";
            //JOptionPane.showMessageDialog(null, "Debe llener el campo Contraseña.");
        }
        else if (pass2Text.equals(new String(""))) {
            Message = "Confirmar";
            //JOptionPane.showMessageDialog(null, "Debe llener el campo Confirmar.");
        }

        //JOptionPane.showMessageDialog(null, Message);
        if (Message == "") {
            //Check if passwords are the same
            if (pass1Text.equals(new String(pass2Text))) {
                if (!this.IsUpdating) {
                    Register reg = new Register();
                    String Response = reg.RegisterUser(userNameText, nameText, lastNameText, numberText, mailText, pass2Text);
                    JOptionPane.showMessageDialog(null, Response);

                    if (Response.equals(new String("Se ha registrado el usuario con existo."))) {
                        this.username.setText("");
                        this.name.setText("");
                        this.lastname.setText("");
                        this.number.setText("");
                        this.mail.setText("");
                        this.password1.setText("");
                        this.password2.setText("");
                    }
                }
                else {
                    //logic to update
                    DashBoard d = new DashBoard();
                    
                    String Response = d.Update(this.id, userNameText, nameText, lastNameText, numberText, mailText, pass2Text);
                    
                    if (Response.equals(new String("Se ha actualizado el usuario con existo."))) {
                        JOptionPane.showMessageDialog(null, Response);
                        this.dispose();
                        DashBoardUser dash = new DashBoardUser();
                        dash.setVisible(true);
                    }    
                }
            }
            else {
                JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden.");
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "Debe llenar los capos: " + Message + ".");
        }
    }//GEN-LAST:event_buttonRegisterActionPerformed

